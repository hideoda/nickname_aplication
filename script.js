  // 表示処理
  function renderList() {
    const list = document.getElementById("nicknameList");
    const count = document.getElementById("count");
  
    list.innerHTML = "";
  
    // 総数表示
    count.textContent = `現在のあだ名数：${nicknames.length}`;
  
    // 一覧表示
    nicknames.forEach(name => {
      const li = document.createElement("li");
  
      li.innerHTML = `
        <span>${name}</span>
        <button class="delete-btn" onclick="deleteNickname('${name}')">
          削除
        </button>
      `;
  
      list.appendChild(li);
    });
  
    saveData();
  }
  
  // あだ名追加
  function addNickname() {
    const input = document.getElementById("nicknameInput");
    const name = input.value.trim();
  
    if (!name) {
      alert("あだ名を入力してください");
      return;
    }
  
    // 重複チェック
    if (nicknames.includes(name)) {
      alert("既に登録されています");
      return;
    }
  
    nicknames.push(name);
  
    input.value = "";
  
    renderList();
  }
  
  // 削除
  let deletedNickname = null;
  
  function deleteNickname(name) {
    const result = confirm(`${name} を削除しますか？`);
  
    if (!result) return;
  
    // 一時保存
    deletedNickname = name;
  
    // 削除
    nicknames = nicknames.filter(n => n !== name);
  
    renderList();
  
    // 元に戻すボタン表示
    document.getElementById("undoArea").style.display = "block";
  }
  
  // 五十音順
  function sortNicknames() {
    nicknames.sort((a, b) => a.localeCompare(b, "ja"));
  
    renderList();
  }
  
  // 保存
  function saveData() {
    localStorage.setItem("nicknames", JSON.stringify(nicknames));
  }

  // 復元
  function undoDelete() {
    if (!deletedNickname) return;
  
    nicknames.push(deletedNickname);
  
    deletedNickname = null;
  
    renderList();
  
    document.getElementById("undoArea").style.display = "none";
  }
  
  // 初期表示
  renderList();